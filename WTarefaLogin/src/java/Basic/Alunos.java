package Basic;

/**
 *
 * @author jjeti
 */
public class Alunos {
    private String nome;
    private String apelido;
    private int idade;
    
    public Alunos(String nome, String apelido, int idade){
        this.nome = nome;
        this.apelido = apelido;
        this.idade = idade;
    }

    public Alunos(){
        this.nome = "";
        this.apelido = "";
        this.idade = 0;
    }
    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the apelido
     */
    public String getApelido() {
        return apelido;
    }

    /**
     * @param apelido the apelido to set
     */
    public void setApelido(String apelido) {
        this.apelido = apelido;
    }

    /**
     * @return the idade
     */
    public int getIdade() {
        return idade;
    }

    /**
     * @param idade the idade to set
     */
    public void setIdade(int idade) {
        this.idade = idade;
    }
    
    
}
